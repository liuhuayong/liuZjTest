package payment.simulator.institution.tx.cmb;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import payment.api.tx.cmb.Tx7262Request;


public class Tx7262 extends HttpServlet {

    private static final long serialVersionUID = 6697471785410636175L;

    @Override
    public void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {

    }

    @Override
    public void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        try {
            // 1.取得参数
            String institutionID = request.getParameter("InstitutionID");
            String txSN = request.getParameter("TxSN");

            // 2.创建交易请求对象
            Tx7262Request tx7262Request = new Tx7262Request();
            tx7262Request.setInstitutionID(institutionID);
            tx7262Request.setTxSN(txSN);

            // 3.执行报文处理
            tx7262Request.process();

            // 4.将参数放置到request对象
            request.setAttribute("plainText", tx7262Request.getRequestPlainText());
            request.setAttribute("message", tx7262Request.getRequestMessage());
            request.setAttribute("signature", tx7262Request.getRequestSignature());
            request.setAttribute("txCode", "7262");
            request.setAttribute("txName", "投资/还款订单单笔代收交易查询");
            request.setAttribute("Flag", "10");
            request.setAttribute("action", request.getContextPath() + "/SendMessage");

            // 5.转向Request.jsp页面
            request.getRequestDispatcher("/Request.jsp").forward(request, response);
        } catch (Exception e) {
            e.printStackTrace();
            processException(request, response, e.getMessage());
        }
    }

    private void processException(HttpServletRequest request, HttpServletResponse response, String errorMessage) {
        try {
            request.setAttribute("errorMessage", errorMessage);
            request.getRequestDispatcher("/ErrorPage.jsp").forward(request, response);
        } catch (Exception e) {
        }
    }

}
